    private void saveStudyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveStudyButtonActionPerformed
        try {
            String uid = uidField.getText();
            Patient patient = (Patient) patientBox.getSelectedItem();
            Practitioner referrer = (Practitioner) referrerBox.getSelectedItem();
            Practitioner interpreter = (Practitioner) interpreterBox.getSelectedItem();
            int reason = Integer.valueOf(reasonCodeField.getText());
            String descr = descriptionField.getText();
            
            if( currentStudy == null ) currentStudy = new Imagingstudy();
            currentStudy.setUid(uid);
            currentStudy.setPatient(patient);
            if( referrer.getIdPractitioner()>= 0 ) currentStudy.setReferrer(referrer);
            else currentStudy.setReferrer(null);
            if( interpreter.getIdPractitioner()>= 0 ) currentStudy.setInterpreter(interpreter);
            else currentStudy.setInterpreter(null);
            currentStudy.setReason(reason);
            currentStudy.setDescription(descr);
            
            ImagingstudyJpaController controller = new ImagingstudyJpaController(emfac);
            if( currentStudy.getIdImagingStudy() == null )
                controller.create(currentStudy);
            else
                controller.edit(currentStudy);
            
            this.dispose();
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(CreateStudyForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(CreateStudyForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveStudyButtonActionPerformed

