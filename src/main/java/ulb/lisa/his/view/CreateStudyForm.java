    public CreateStudyForm(EntityManagerFactory emfac, List<Practitioner> allPractitioners, List<Patient> allPatients, Imagingstudy currentStudy) {
        this.emfac = emfac;
        this.currentStudy = currentStudy;
        
        patientModel = new DefaultComboBoxModel();
        referrerModel = new DefaultComboBoxModel();
        interpreterModel = new DefaultComboBoxModel();
        
        if( allPractitioners == null ){
            PractitionerJpaController pracController = new PractitionerJpaController(emfac);
            allPractitioners = pracController.findPractitionerEntities();
        }
        if( allPatients == null ){
            PatientJpaController patientController = new PatientJpaController(emfac);
            allPatients = patientController.findPatientEntities();
        }
                
        initComponents();
        
        Practitioner noprac = new Practitioner(-1);
        noprac.setPerson(new Person(-1));
        Patient nopat = new Patient(-1);
        nopat.setPerson(new Person(-1));
        
        fillModel(patientModel, null, allPatients, patientBox);
        fillModel(referrerModel, noprac, allPractitioners, referrerBox);
        fillModel(interpreterModel, noprac, allPractitioners, interpreterBox);
    }
    
    private void fillModel(DefaultComboBoxModel model, Object firstElement, List elements, JComboBox box){
        if( firstElement != null ) model.addElement(firstElement);
        
        for( Object e : elements ) model.addElement(e);
        
        box.setModel(model);
    }
    

